  constructor(@Inject(LOCALE_ID) private locale: string) {
    this.initializeLanguage();
  }

  private initializeLanguage(): void {
    const savedLanguage = localStorage.getItem(this.LANGUAGE_KEY) as Language;
    let initialLanguage: Language = 'en';

    if (savedLanguage && ['en', 'ar', 'es', 'fr'].includes(savedLanguage)) {
      initialLanguage = savedLanguage;
    } else if (this.locale.startsWith('ar')) {
      initialLanguage = 'ar';
    } else if (this.locale.startsWith('es')) {
      initialLanguage = 'es';
    } else if (this.locale.startsWith('fr')) {
      initialLanguage = 'fr';
    }

    this.languageSignal.set(initialLanguage);
    this.applyLanguage(initialLanguage);
  }

  get language() {
    return this.languageSignal;
  }

  get direction() {
    return this.directionSignal;
  }

  get currentLanguage(): Language {
    return this.languageSignal();
  }

  get currentDirection(): Direction {
    return this.directionSignal();
  }

  setLanguage(language: Language): void {
    this.languageSignal.set(language);
    this.saveLanguage(language);
    this.applyLanguage(language);
    this.languageChangeSubject.next(language);
  }

  toggleLanguage(): void {
    const languages: Language[] = ['en', 'ar', 'es', 'fr'];
    const currentIndex = languages.indexOf(this.currentLanguage);
    const nextIndex = (currentIndex + 1) % languages.length;
    this.setLanguage(languages[nextIndex]);
  }

  translate(key: string): string {
    return this.translations[this.currentLanguage][key] || key;
  }

  isRTL(): boolean {
    return this.currentDirection === 'rtl';
  }

  private saveLanguage(language: Language): void {
    localStorage.setItem(this.LANGUAGE_KEY, language);
  }

  private applyLanguage(language: Language): void {
    document.documentElement.lang = language;
    document.documentElement.dir = language === 'ar' ? 'rtl' : 'ltr';
  }
